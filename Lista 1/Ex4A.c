// Exercício 4a) Leia um número e imprima seu sucessor


#include <stdio.h.>
#include <stdlib.h>
#include <math.h>

int main () {
    int primeiro,sucessor;
    printf("Escreva o numero:\n");
    scanf("%i",&primeiro);
    sucessor=primeiro+1;
    printf("sucessor=%i\n",sucessor);
    return 0;
}

// TESTE
// Entrada: 2
// Saída: 3


